//OutDAC.c
#include "OutDAC.h"

void OutDAC (int sample){

	
	if((sample&(0x00000001))==0x00000001)
		pADI_GP0->GPSET=GPSET_SET0_SET;
	else
		pADI_GP0->GPCLR=GPCLR_CLR0_CLR;
		
	if((sample&(0x00000002))==0x00000002)
		pADI_GP0->GPSET=GPSET_SET1_SET;
	else
		pADI_GP0->GPCLR=GPCLR_CLR1_CLR;
	
	if((sample&(0x00000004))==0x00000004)
		pADI_GP0->GPSET=GPSET_SET2_SET;
	else
		pADI_GP0->GPCLR=GPCLR_CLR2_CLR;
	
	if((sample&(0x00000008))==0x00000008)
		pADI_GP0->GPSET=GPSET_SET3_SET;
	else
		pADI_GP0->GPCLR=GPCLR_CLR3_CLR;
	
	if((sample&(0x00000010))==0x00000010)
		pADI_GP0->GPSET=GPSET_SET4_SET;
	else
		pADI_GP0->GPCLR=GPCLR_CLR4_CLR;
	
	if((sample&(0x00000020))==0x00000020)
		pADI_GP0->GPSET=GPSET_SET5_SET;
	else
		pADI_GP0->GPCLR=GPCLR_CLR5_CLR;
	
	if((sample&(0x00000040))==0x00000040)
		pADI_GP0->GPSET=GPSET_SET6_SET;
	else
		pADI_GP0->GPCLR=GPCLR_CLR6_CLR;
	
	if((sample&(0x00000080))==0x00000080)
		pADI_GP0->GPSET=GPSET_SET7_SET;
	else
		pADI_GP0->GPCLR=GPCLR_CLR7_CLR;
	
	
	if((sample&(0x00000100)==0x00000100))
		pADI_GP1->GPSET=GPSET_SET0_SET;
	else
		pADI_GP1->GPCLR=GPCLR_CLR0_CLR;
		
	if((sample&(0x00000200)==0x00000200))
		pADI_GP1->GPSET=GPSET_SET1_SET;
	else
		pADI_GP1->GPCLR=GPCLR_CLR1_CLR;
	
	if((sample&(0x00000400)==0x00000400))
		pADI_GP1->GPSET=GPSET_SET2_SET;
	else
		pADI_GP1->GPCLR=GPCLR_CLR2_CLR;
	
		
	if((sample&(0x00000800)==0x00000800))
		pADI_GP1->GPSET=GPSET_SET3_SET;
	else
		pADI_GP1->GPCLR=GPCLR_CLR3_CLR;
	
	if((sample&(0x00001000)==0x00001000))
		pADI_GP1->GPSET=GPSET_SET4_SET;
	else
		pADI_GP1->GPCLR=GPCLR_CLR4_CLR;
		
	if((sample&(0x00002000)==0x00002000))
		pADI_GP1->GPSET=GPSET_SET5_SET;
	else
		pADI_GP1->GPCLR=GPCLR_CLR5_CLR;
}
